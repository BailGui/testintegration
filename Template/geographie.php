    <p class="textaccueil">La tribu thrace des Serdes, donna son nom au premier établissement urbain établi sur le site : Serdica au viie siècle av. J.-C.. Puis les Bulgares l'appelèrent Stredets (la ville située au milieu). Les Byzantins la nommèrent Triaditsa.
En 1376, elle prit son nom actuel Sofia, d'après la basilique Sainte-Sophie.
<br><br>
Sofia est située dans l'Ouest du pays. Au sud de la ville se trouve le massif du Vitocha qui culmine à 2 290 m (Černi vrăh - Pic Noir) et qui est un lieu d'excursion apprécié des Sofiotes.
<br><br>
À 25 km environ au nord de la ville commencent les montagnes des Balkans qui s'étendent d'ouest en est sur 500 km à travers toute la Bulgarie.
<br><br>
La ville est traversée par la rivière Iskar, affluent de la rive droite du Danube.
<br><br>
Sofia connaît un climat continental, avec des grandes amplitudes dans une journée, et des grandes différences d'une année à l'autre. En effet, elle se situe à la limite des hivers sibériens que connaissent les pays de l'Est, et la clémence des hivers des pays méditerranéens. La Bulgarie jouit d'un climat complexe et Sofia en est un exemple frappant. La température annuelle moyenne est de 10,6 °C et les précipitations annuelles moyennes sont de 581,8 mm.
<br><br>
Juillet et août sont les mois les plus chauds avec une température moyenne de 21 °C. Janvier et février sont les plus secs avec des précipitations moyennes de 33 et 31 mm.
<br><br>
Les précipitations les plus importantes sont en mai et juin avec respectivement 67 et 75 mm. Le mois le plus froid est celui de janvier avec une température moyenne de −0,5 °C
